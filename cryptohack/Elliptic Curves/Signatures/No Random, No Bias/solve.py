B = 1 << 160

ts, us = [], []
for sig in sigs:
    h = bytes_to_long(sha1(sig['msg'].encode()).digest())
    r, s = sig['r'], sig['s']
    sinv = pow(s, -1, q)
    ts.append((r * sinv) % q)
    us.append((h * sinv) % q)


# Kannan embedding for HNP. Original lattice in Z^3 generated by
#   q*e_1, q*e_2, q*e_3, (t1,t2,t3)   (4 generators, rank 3).
# Target b_i = -u_i - B/2 (mod q), so the closest lattice vector v satisfies
#   v_i - b_i = d*t_i + u_i + B/2 - m_i*q = k_i - B/2 ∈ [-B/2, B/2).
# Embed CVP into SVP with weight W = B/2 in last column.
W = B // 2
bs = [(-u - W) % q for u in us]
M = [
    [q,     0,     0,     0],
    [0,     q,     0,     0],
    [0,     0,     q,     0],
    [ts[0], ts[1], ts[2], 0],
    [bs[0], bs[1], bs[2], W],
]


def lll(basis_in):
    """Standard textbook LLL using Fractions (exact)."""
    from fractions import Fraction
    basis = [[Fraction(x) for x in row] for row in basis_in]
    n = len(basis)
    m = len(basis[0])

    def gram_schmidt(b):
        ortho = []
        mu = [[Fraction(0)] * n for _ in range(n)]
        for i in range(n):
            v = list(b[i])
            for j in range(i):
                num = sum(b[i][k] * ortho[j][k] for k in range(m))
                den = sum(ortho[j][k] * ortho[j][k] for k in range(m))
                if den == 0:
                    mu[i][j] = Fraction(0)
                else:
                    mu[i][j] = num / den
                v = [v[k] - mu[i][j] * ortho[j][k] for k in range(m)]
            ortho.append(v)
        return ortho, mu

    delta = Fraction(3, 4)
    ortho, mu = gram_schmidt(basis)
    k = 1
    while k < n:
        for j in range(k - 1, -1, -1):
            if abs(mu[k][j]) > Fraction(1, 2):
                qq = int(round(mu[k][j]))
                basis[k] = [basis[k][i] - qq * basis[j][i] for i in range(m)]
                ortho, mu = gram_schmidt(basis)
        lhs = sum(ortho[k][i] * ortho[k][i] for i in range(m))
        rhs = (delta - mu[k][k - 1] ** 2) * sum(
            ortho[k - 1][i] * ortho[k - 1][i] for i in range(m)
        )
        if lhs >= rhs:
            k += 1
        else:
            basis[k], basis[k - 1] = basis[k - 1], basis[k]
            ortho, mu = gram_schmidt(basis)
            k = max(k - 1, 1)
    return [[int(x) for x in row] for row in basis]


reduced = lll(M)


def try_recover_d():
    # Short vector last coord should be ±W. The other coords represent
    # the centered nonces; try several sign / offset combinations and
    # validate against the public key.
    pub_x = 48780765048182146279105449292746800142985733726316629478905429239240156048277
    pub_y = 74172919609718191102228451394074168154654001177799772446328904575002795731796
    h0 = bytes_to_long(sha1(sigs[0]['msg'].encode()).digest())
    r0, s0 = sigs[0]['r'], sigs[0]['s']
    s0_inv_r0_inv = pow(r0, -1, q)
    for row in reduced:
        if row[3] == 0:
            continue
        if abs(row[3]) % W != 0:
            continue
        for k0_candidate in (
            row[0] + W, -row[0] + W, row[0], -row[0], row[0] - W, -row[0] - W
        ):
            k0 = k0_candidate % q
            if k0 == 0 or k0 >= B:
                continue
            d = ((s0 * k0 - h0) * s0_inv_r0_inv) % q
            P = d * G
            if int(P.x()) == pub_x and int(P.y()) == pub_y:
                return d, k0
    return None, None


