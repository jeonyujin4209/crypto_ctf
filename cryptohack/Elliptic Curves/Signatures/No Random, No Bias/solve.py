"""
No Random, No Bias — ECDSA on P-256 with deterministic nonce
    nonce = sha1(privkey || sha1(msg)).
SHA-1 is 160 bits but the group order q is 256 bits, so every nonce is at
most 160 bits. This is a 96-bit Hidden Number Problem.

With n=3 signatures the Boneh-Venkatesan / Kannan-embedding lattice (4x4)
yields a short vector containing the unknown nonces k_i.

ECDSA gives:  s_i * k_i = h_i + r_i * d (mod q)
Let t_i = r_i * s_i^{-1}, u_i = h_i * s_i^{-1}, then
    k_i = t_i * d + u_i  (mod q),       0 <= k_i < B = 2^160.

Lattice (rows):
    [ q   0   0   0 ]
    [ 0   q   0   0 ]
    [ t1 t2  t3   0 ]
    [ u1 u2  u3   B ]      <- last row is the embedding "target"
A short combination of the form  "row3*d + row4 - sum m_i*q*e_i"  yields the
vector (k1, k2, k3, B). LLL on this 4x4 basis recovers it.

After d is recovered, decrypt the hidden flag:
    T = d*Q with Q = (flag_int, y) on P-256, so Q = d^{-1}*T (mod q).
"""

from hashlib import sha1
from Crypto.Util.number import bytes_to_long, long_to_bytes
from ecdsa.ecdsa import curve_256, generator_256
from ecdsa import ellipticcurve

