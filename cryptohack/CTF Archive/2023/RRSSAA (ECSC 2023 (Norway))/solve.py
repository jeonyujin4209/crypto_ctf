"""
RRSSAA — ECSC 2023 (Norway)

Vulnerability:
  N = prod(get_prime(i) for i in range(2, 128))
  Each prime is generated by random.Random() seeded with secrets.randbelow(2**i),
  i.e. seed has at most i bits of entropy. For small i (i<=20 or so), seed space
  is tiny enough to brute force.

Attack:
  1. Brute force seeds 0..2^K. For each seed, replay get_prime(seed) and check if
     the resulting prime divides N. Each found prime p_i is a factor of N.
  2. Since flag m has only 128 bytes = 1024 bits, we don't need ALL ~126 prime
     factors — just enough so that the product of found primes exceeds 2^1024.
  3. For each found prime p_i, compute m mod p_i via:
        d_i = e^(-1) mod (p_i - 1)
        m_i = c^d_i mod p_i
  4. CRT the m_i's to get m mod (prod p_i). Since prod p_i > 2^1024 > m, this is m.
"""
import random
import gmpy2
from Crypto.Util.number import long_to_bytes


def get_prime_with_seed(seed):
    p = 1
    r = random.Random()
    r.seed(seed)
    while not gmpy2.is_prime(p):
        p = r._randbelow(2**256) | 1
    return int(p)


def main():
    with open("output.txt") as f:
        line = f.read().strip()
    parts = line.split()
    N = int(parts[0], 16)
    C = int(parts[1], 16)
    e = 0x10001

    # Brute force seeds to find primes dividing N
    # Need product > 2^1024 (flag is 128 bytes). 5 primes is enough; we'll find ~15+ with 2^16 seeds.
    SEED_LIMIT = 2**16
    print(f'[*] Brute forcing {SEED_LIMIT} seeds...')
    primes = []
    n_remaining = N
    for s in range(SEED_LIMIT):
        p = get_prime_with_seed(s)
        if n_remaining % p == 0:
            primes.append(p)
            n_remaining //= p
            prod = 1
            for q in primes:
                prod *= q
            print(f'  seed={s}, total_primes={len(primes)}, product_bits={prod.bit_length()}')
            if prod.bit_length() >= 1100:
                # Slightly more than 1024 for safety
                break

    print(f'[*] Found {len(primes)} primes')

    # CRT recover m mod (prod primes)
    print('[*] Computing m_i = c^d_i mod p_i for each prime...')
    residues = []
    moduli = []
    for p in primes:
        # If gcd(e, p-1) > 1, e is not invertible mod p-1; skip this prime
        if gmpy2.gcd(e, p - 1) != 1:
            print(f'  Skipping prime with gcd(e, p-1) = {gmpy2.gcd(e, p-1)}')
            continue
        d = pow(e, -1, p - 1)
        m_i = pow(C, d, p)
        residues.append(m_i)
        moduli.append(p)

    # CRT
    print(f'[*] CRT over {len(moduli)} primes...')
    M = 1
    for m in moduli:
        M *= m

    m = 0
    for r, p in zip(residues, moduli):
        Mi = M // p
        Mi_inv = pow(Mi, -1, p)
        m = (m + r * Mi * Mi_inv) % M

    print(f'[*] Recovered m, bit_length={m.bit_length()}')

    # Convert to bytes
    flag = long_to_bytes(m)
    print('[+] Flag:', flag)


if __name__ == '__main__':
    main()
