"""
Truncated-LCG seed recovery via LLL + Babai's nearest-plane.

Given an LCG  x_{i+1} = a*x_i + b (mod m)  with m a power of two, plus
observations of the top `out_bits` bits of n consecutive states (states 1..n),
recover the seed (= the state at index 0).

This is a Hidden Number Problem instance solved with the standard
truncated-LCG attack (Frieze-Håstad-Kannan-Lagarias 1988):

  l_i = (A'_i * l_1 + C'_i) mod m,    0 ≤ l_i < W = 2^(k - out_bits)

where l_i is the unknown low (k - out_bits) bits of state_i, A'_i = a^(i-1),
and C'_i is a fully-known constant (it depends only on the publicly observed
top bits and the LCG parameters). Set up the lattice generated by

    b_1 = (1, A'_2, A'_3, ..., A'_n)
    b_2 = (0, m,   0,   ..., 0   )
    b_3 = (0, 0,   m,   ..., 0   )
    ...
    b_n = (0, 0,   0,   ..., m   )

and run Babai's nearest-plane against the target

    t = (W/2, W/2 - C'_2, W/2 - C'_3, ..., W/2 - C'_n)

The closest lattice vector has first coordinate exactly l_1, from which we
recover state_1 = top_1 * W + l_1 and then state_0 = a^{-1} * (state_1 - b).
"""

from __future__ import annotations

from fractions import Fraction
from typing import List, Sequence

import olll  # pure-Python LLL


def _innerprod(u: Sequence, v: Sequence) -> Fraction:
    return sum(Fraction(a) * Fraction(b) for a, b in zip(u, v))


def _gram_schmidt(B: List[List[int]]):
    """Returns Bs (rationals) and mu coefficients."""
    n = len(B)
    Bs: List[List[Fraction]] = [[Fraction(x) for x in row] for row in B]
    mu = [[Fraction(0)] * n for _ in range(n)]
    for i in range(n):
        for j in range(i):
            mu[i][j] = _innerprod(B[i], Bs[j]) / _innerprod(Bs[j], Bs[j])
            for k in range(len(B[0])):
                Bs[i][k] -= mu[i][j] * Bs[j][k]
    return Bs, mu


def babai_nearest_plane(B: List[List[int]], target: Sequence[int]) -> List[int]:
    """Babai's nearest-plane CVP approximation; B should already be LLL-reduced."""
    n = len(B)
    Bs, _ = _gram_schmidt(B)
    v = [Fraction(x) for x in target]
    for i in range(n - 1, -1, -1):
        c = _innerprod(v, Bs[i]) / _innerprod(Bs[i], Bs[i])
        c_int = round(c)
        for k in range(len(B[0])):
            v[k] -= c_int * B[i][k]
    closest = [int(target[k] - v[k]) for k in range(len(target))]
    return closest


def recover_truncated_lcg_seed(
    a: int,
    b: int,
    m: int,
    top_bytes: Sequence[int],
    out_bits: int = 8,
) -> int | None:
    """
    Returns `state_0` such that x_1 = a*state_0 + b mod m, x_2 = a*x_1 + b mod m,
    ..., and `(x_i >> (k - out_bits)) & ((1 << out_bits) - 1) == top_bytes[i-1]`.
    """
    n = len(top_bytes)
    k_bits = m.bit_length() - 1
    assert m == (1 << k_bits)
    low_bits = k_bits - out_bits
    W = 1 << low_bits

    # Compute the recurrence on the *low parts*:
    #     state_i  = top_i * W + l_i,   l_i ∈ [0, W)
    #     l_{i+1} = (a*l_i + d_i) mod m
    # where d_i = (a * top_i * W + b - top_{i+1} * W) mod m.
    #
    # Then iteratively, l_i = A'_i * l_1 + C'_i mod m.
    # A'_1 = 1 (the seed of the recurrence), C'_1 = 0.
    # A'_{i+1} = a * A'_i mod m, C'_{i+1} = (a * C'_i + d_i) mod m.
    A_prime = [1]
    C_prime = [0]
    for i in range(n - 1):
        d_i = (a * top_bytes[i] * W + b - top_bytes[i + 1] * W) % m
        A_prime.append((a * A_prime[-1]) % m)
        C_prime.append((a * C_prime[-1] + d_i) % m)

    # Lattice basis (n × n)
    basis: List[List[int]] = []
    row1 = [0] * n
    row1[0] = 1
    for j in range(1, n):
        row1[j] = A_prime[j]
    basis.append(row1)
    for j in range(1, n):
        row = [0] * n
        row[j] = m
        basis.append(row)

    reduced = olll.reduction(basis, 0.75)

    # Target: closest vector should have entries (l_1, l_2, ..., l_n) with each
    # l_i ∈ [0, W). Pick the centre of that range to help Babai converge.
    target = [W // 2 - C_prime[i] for i in range(n)]
    target[0] = W // 2  # for the seed slot, no constant offset

    closest = babai_nearest_plane(reduced, target)

    # closest[0] should be l_1
    l1 = closest[0] % m
    if not (0 <= l1 < W):
        # try wrapping
        return None

    state_1 = top_bytes[0] * W + l1
    if not _verify_from_state1(a, b, m, state_1, top_bytes, out_bits):
        return None

    # state_0 = a^{-1} * (state_1 - b) mod m
    a_inv = pow(a, -1, m)
    seed = (a_inv * (state_1 - b)) % m
    return seed


def _verify_from_state1(a, b, m, state_1, top_bytes, out_bits):
    low_bits = m.bit_length() - 1 - out_bits
    state = state_1
    if (state >> low_bits) != top_bytes[0]:
        return False
    for top in top_bytes[1:]:
        state = (a * state + b) % m
        if (state >> low_bits) != top:
            return False
    return True


def _self_test() -> None:
    a = 0x1337DEADBEEF
    b = 0xB
    m = 1 << 48
    import random

    rng = random.Random(0)
    ok_count = 0
    for trial in range(5):
        seed = rng.randrange(m)
        state = seed
        tops = []
        for _ in range(8):
            state = (a * state + b) % m
            tops.append((state >> 40) & 0xFF)
        recovered = recover_truncated_lcg_seed(a, b, m, tops)
        ok = recovered == seed
        ok_count += int(ok)
        print(f"trial {trial}: seed={seed:012x} recovered={recovered if recovered is None else f'{recovered:012x}'} ok={ok}")
    print(f"trunc_lcg self-test: {ok_count}/5")


if __name__ == "__main__":
    _self_test()
